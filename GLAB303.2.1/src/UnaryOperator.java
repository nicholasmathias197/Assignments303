public class UnaryOperator {
    public static void main (String arg[]){
        int sum = +1;

        System.out.println(sum);

        sum --;
        System.out.println(sum);

        sum++;
        System.out.println(sum);

        sum = -sum;
        System.out.println(sum);

        boolean result = false;
        System.out.println(result);

        System.out.println(!result);
    }
}
